    void tampil(){
        DefaultTableModel tabelmodel = new DefaultTableModel();
        tabelmodel.addColumn("Nama");
        tabelmodel.addColumn("jk");
        tabelmodel.addColumn("jk");
        tabelmodel.addColumn("Prodi");
        try {
            koneksi();
            String sql = "select * from mahasiswa";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                tabelmodel.addRow(new Object[]{
                    rs.getString("nobp"),
                    rs.getString("jk"),
                    rs.getString("nama"),
                    rs.getString("prodi")
                });
                jTable1.setModel(tabelmodel);
            }
            jTable1.setModel(tabelmodel); // set model tabel setelah selesai mengisi data
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan waktu panggil data");
        }
    }
