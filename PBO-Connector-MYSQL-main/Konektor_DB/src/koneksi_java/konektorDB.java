    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        try {
            if (jTextField2.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "nama tidak boleh kosong");
                jTextField2.getText().isEmpty();
                JOptionPane.showMessageDialog(null, "Data tidak Berhasil Disimpan");
            } else if (jTextField3.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "no bp tidak boleh kosong");
                jTextField3.getText().isEmpty();
                JOptionPane.showMessageDialog(null, "Data tidak Berhasil Disimpan");
            } else if (jTextField4.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "jurusan tidak boleh kosong");
                jTextField4.getText().isEmpty();
                JOptionPane.showMessageDialog(null, "Data tidak Berhasil Disimpan");
            } else {
                koneksi();
                String nama = jTextField2.getText();
                String no_bp = jTextField3.getText();
                String prodi = jTextField4.getText();
                String jk = jComboBox1.getSelectedItem().toString();

                String sql = "INSERT INTO mahasiswa (no_bp, nama, prodi,jk) "
                        + "VALUES ('" + no_bp + "', '" + nama + "','" + prodi + "','" + jk + "')";
                st.executeUpdate(sql);
                con.close();
                tampil();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menyimpan data sudah ada");
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

